package day1;

public class Program3_DatatypeExample {

	public static void main(String[] args) {
		
		//boolean takes 1 bit
		boolean result=false;
		System.out.println("result");
		
		//byte takes 1 byte=8bits
		//-128 to 127
		byte ans=-128;
		System.out.println("ans");
		
		//short takes 2 bytes
		//-32000 to 32000
		short data =-32000;
		System.out.println("data");
		
		//char takes 2 bytes
		char alpha ='A';
		System.out.println("alpha");
		
		// its take 8 byte
		long PhoneNumber=9876543210l;
		System.out.println(PhoneNumber);
		
		// its takes 4 bytes , total digit floating is 7
		float marks1=98.122324324f;
		System.out.println(marks1);
		
		float marks2=98456.12232f;
		System.out.println(marks2);
		
		// 8 bytes 15 floating 
		double data1=1234.123456789012345;
		System.out.println(data1);
		
		
		
	}

}
