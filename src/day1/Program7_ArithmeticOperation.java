package day1;

public class Program7_ArithmeticOperation {

	public static void main(String[] args) {
		
		int num1=5;
		int num2=2;
		
		System.out.println(num1+num2);
		System.out.println(num2-num1);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		System.out.println(num1%num2);

	}

}
